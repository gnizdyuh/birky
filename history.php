				<h3>Адміністративний поділ</h3>
				<ul type="disc">
					<li>XV—XVI ст. королівщина Борки входила до Теребовлянського староства (повіту), Галицької землі, Руського воєводства, Корони Королівства Польського.
					<li>1569—1772 — у складі Речі Посполитої.</li>
					<li>Після Першого поділу Речі Посполитої 1772 під владою Габсбурзької монархії.</li>
					<li>Від початку 1782 р. с. Борки увійшло до складу Тернопільського повіту, Тернопільського округу («циркулу»), Королівства Галичини та Володимирії.
					<li>Від 15 червня 1810 до 3 травня 1815 віддано Росії в складі новоутвореного Тернопільского циркулу Тернопільського краю.</li>
					<li>9 червня 1815, після Віденського конґресу, повернуто Габсбурзькій монархії.</li>
					<li>Під час Першої світової війни від 21 серпня 1914 р. до 25 липня 1917 р. Великі Бірки перебувають під російською окупацією в складі Тернопільської губернії, Галицько-Буковинського генерал-губернаторства.</li>
					<li>З початком Листопадового зриву від 1 листопада 1918 до 17 липня 1919 р. — в складі ЗУНР.</li>
					<li>Під час Польсько-радянської війни 1920 р. від 26 липня до 21 серпня село перебуває в складі ГСРР.</li>
					<li>1 вересня 1921 р. входить до Тарнопільського повіту, в складі новоутвореного Тарнопільського воєводства Другої Речі Посполитої.</li>
					<li>1 серпня 1934 утворено сільську гміну Борки Великі (пол. Borki Wielkie), в яку увійшли 11 навколишніх сільських громад Тарнопільського повіту.
					<li>Від 4 грудня 1939 р. входить до Тернопільської області УРСР.</li>
					<li>2 липня 1941 — 21 березня 1944 рр. під час німецько-фашистської окупації від 1 серпня 1941 Великі Бірки — центр волости Тернопільського повіту Крайсгауптманшафту Тернопіль Дистрикту Галичина Генеральної губернії Третього Райху.</li>
					<li>Від 17 січня 1940 р. до 2 липня 1941 і від 21 березня 1944 до 1 січня 1963 р. — районний центр Великоборківського району Тернопільської області.</li>
					<li>У 1963–1966 рр. с. Великі Бірки входило до Підволочиського району.</li>
					<li>Від 8 грудня 1966 р. — до Тернопільського району.</li>
			   	</ul>
