			<h1>Історія нашого селища</h1>
			<hr class="topicHR">
			<div class="wrapper">
				<div id="panorama"></div>
				<div class="hblock">
					<p><strong>Вели́кі Бі́рки</strong> (англ. Velýki Bírky, пол. Borki Wielkie, рос. Вели́кие Бо́рки) — селище міського типу у Тернопільському районі, Тернопільської області. Розташоване на обох берегах річки Гнізни Гнилої верхньої частини Гнізни — лівої притоки Серету, за 12 км на схід від Тернополя. Через територію селища протікають річки Теребна та Гніздечна — ліва і права притоки Гнізни Гнилої.</p>
				</div>	
				<hr>
				<div class="hblock">
				<h3>Назва</h3>
					<p>Сучасна назва Бірки походить від староукраїнського слова бір, що означає сосновий ліс. У XV ст. між річками Серетом і Гнізною був великий ліс, тому селище дістало назву Бо́рки від місцевості де воно виникло, згодом назви трансформувались: у XVI—XVII ст. містечко Бо́рки, у другій половині XVII ст. — село Подбор'є, Бо́рки, від середини 30-х рр. XIX ст. Бо́рки вели́кі, згодом Вели́кі Бо́рки , від 1967 р. с. Вели́кі Бі́рки, від 27 травня 1978 р.  — смт Вели́кі Бі́рки.</p>
				</div>
				<hr>
				<div class="hblock">	
				<h3>Археологічні знахідки</h3>
					<p>Територія сучасних Великих Бірок була заселена з часів пізнього палеоліту. До найдавніших археологічних знахідок виявлених на території селища відносяться крем'яні знаряддя праці та їх уламки доби неоліту, які впродовж багатьох років знаходять в полях західніше селища та на пагорбах в межиріччі річок Гнізни Гнилої і Гніздечної, крем'яні та кам'яні сокири епохи енеоліту (мідно-камяної доби 3-2 тис. р. до н. е.) одна з яких, знайдена в 1951 р., експонується у відділі стародавньої історії Тернопільського обласного краєзнавчого музею. Знайдена восени 2009 р. мідна сокира кавказького типу відноситься до рідкісних знахідок епохи ранньої бронзи (енеоліту). У 2012 р. на південно-західному схилі на відстані близько 300 м на захід від селища, в полі на глибині 0,4-0,5 м, знайдена давня кінська упряж, що відноситься до епохи раннього заліза (VIII—VII ст. до н. е.). На околицях Великих Бірок виявлено також могильник 8-6 ст. до н. е. На початку ХХ ст. в експозиціях Подільського музею (тепер ТОКМ) зберігався глиняний посуд періоду висоцької культури (1100—600 рр. до н. е.) знайдений на території селища, але втрачений під час Другої світової війни. Залишки одного з могильників черняхівської культури було випадково виявлено при виконанні земляних робіт, під час будівництва лікарні в центрі селища, восени 1987 р., коли будівельники натрапили на давнє трупопокладення. На місце знахідки прибула Черняхівська експедиція ТОКМ на чолі з відомим археологом Ігорем Геретою, який дослідив і описав це поховання. Є всі підстави вважати, що на теперішній території Великих Бірок у ІІІ — V ст. н. е. існувало поселення черняхівської культури, яке займало обширну площу вздовж південно-західної похилої ділянки лівого берега річки Гнізни Гнилої.</p>
				</div>
				<hr>
				<div class="hblock">
				<h3>Період Середньовіччя</h3>
					<p>Під Короною і Річчю Посполитою</p>
					<p>Перша писемна згадка про поселення датована 10 березня 1410 р., коли, за часів правління короля Владислава ІІ Ягайла, воно входило до Корони Польської. Староста подільський і теребовлянський Пйотр Влодкович (Владкович) у Теребовлі 10 березня 1410 р. підтвердив уклад границі, яка розмежовувала село Чернилів (тепер Чернелів-Руський) від Борка. Щоправда, у Літописі руському згадується битва русько-галицького війська з монголами 1243 р. в Борку; тож, можливо, тут вперше мовиться про нинішні Великі Бірки чи територію, де вони розташовані.</p>
					<p>З польських джерел відомо, що у XV—XVI ст. королівщина Борек разом із 4-ма навколишніми селами входили до Теребовлянського староства (повіту), Галицької землі, Руського воєводства, Королівства Польського. 15 червня 1530 року підстароста Кам'янецький Ян Срочіцкі (Срочицький) дістав дозвіл від короля Сигізмунда І Старого закласти на місці села Борек місто на магдебурзькому праві.</p>
					<p>Однак, він чомусь не спромігся на закладення міста на тому місці, тому в короткому часі змушений уступити ці маєтки галицькому підкоморію Миколаю Сенявському, якому король надав дозвіл на викуп з рук Яна Срочицького королівського села (тоді ще не міста) Борек разом з належними до нього селами: Дичковом, Ходачковом, або Качавою (тепер Малий Ходачків), Галущинцями, і Заднишівкою (тепер в межах смт Підволочиська). У його власності воно перебувало близько 30-ти років, проте Миколай Сенявський, ставши великим коронним гетьманом, володіючи багатьма іншими маєтностями, не прив'язався до Борка. З 1565 р., за часів короля Сигізмунда Авґуста ІІ, містечко Борек разом із сусідніми селами стало власністю королівського управителя Кам'янецького замку Миколая Потоцького і перебувало під пануванням родини магнатів Потоцьких понад 100 років.</p>
					<p>Згідно з люстрацією королівщини Борек, проведеною в 1565 р., населення містечка становило близько 550 душ, які утримували в своїх господарствах 720 волів, 720 овець, 480 свиней і 320 вуликів. В містечку була православна церква св. вмч. Параскевії П'ятниці, а піп сплачував податок в 1 злотий. Працювали також млин, гуральня, також виготовляли солод і розводили рибу в ставку. Фільварок знаходився в сусідньому селі Дичкові, що входило до Борківського маєтку. Загальний дохід пана з містечка становив 273 злотих 23 грошів. Винекненню містечка Борек і його розвитку в XVI ст. сприяло його вигідне розташування поблизу кордону між Короною і Волинськими землями, що входили тоді до Великого князівства Литовського. В той час там установились сприятливі політичні і господарські відносини, процвітала торгівля, діяли митниці, вздовж кордону будувались оборонні укріплення, замки і замочки. Але цей період виявився нетривалим. З прийняттям в 1569 Люблінської унії та утворенням федеративної держави — Речі Посполитої щезли границі з Литвою, митниці ліквідували, прикордонна торгівля занепала. З початком XVII ст. посилилсь татарські набіги, які в тих часах, часто кілька разів на рік, заливали Галицьку землю вбивствами, грабунками та руйнуваннями. Містечко Борек, разом з сусідніми селами, в короткому часі двічі було спустошене татарами у 1624 і 1626 рр.</p>
				</div>
