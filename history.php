					Пам'ятники, пам'ятні знаки та меморіали Великих Бірок:
					<ul type="disc"> 
				    <li>Пам'ятний хрест, про скасування панщини в Галичині, і проведення селянської реформи, внаслідок Революції 1848 р.</li>
					    <li>Хрест про заснування товариства тверезості 1878 р. (знаходиться на подвір'ї церкви святої Параскеви П'ятниці).</li>
					    <li>На військовому цвинтарі, де у братських могилах захоронені понад 1 тис. воїнів Червоної армії, встановлено пам'ятники:</li>
					    <ul type="disc">
					    	<li>воїнам ЧА, які загинули на території Великоборківського району (1955);
					        <li>воїнам односельчанам, полеглим під час Німецько-радянської війни, 1967 р.</li></ul>
					    <li>У 1992 р. активістами місцевого осередку Народного руху України на старому цвинтарі споруджено меморіал борцям за волю України, до якого належать дві насипні могили, з хрестами на вершинах, та пам'ятна стела з написом: «Тут впали за волю сини України тут наша історія слава і честь». Ліва могила з березовим хрестом насипана в пам'ять про полеглих Українських січових стрільців та воїнів УГА, права на місці перезахоронення 34-х полонених вояків УПА, підпільників ОУН та заложників розстріляних гітлерівцями в селищі в січні 1944 року. Архітектор Павло Галоха.</li>
					    <li>В неділю, 14 травня 1995 року, в центрі селища урочисто відкрито пам'ятник Т. Г. Шевченку, виготовлений за рахунок добровільних внесків жителів селища та місцевих підприємств. Скульптор Микола Невеселий, архітектор П. Галоха.</li>
					    <li>У березні 1998 року на центральному корпусі Великобірківській ЗОШ І-ІІІ ст. ім. академіка Степана Балея урочисто встановлена іменна меморіальна дошка.</li>
