				<div class="center"><a href="images/Scool_VB.jpg"><img src="images/Scool_VB_small.jpg"></a></div>
				<div class="hblock">
					<p>НВК Великобірківська ЗОШ І-ІІІ ст. школа-гімназія ім. С. Балея   — навчально-виховний комплекс Великобірківська загальноосвітня І-ІІІ-го ступенів школа-гімназія ім. С. Балея є однією з найстарших і найбільших шкіл у Тернопільському районі, знаходиться за адресою вул. Шкільна, 1 у смт Великі Бірки. Школу відвідують учні як з Великих Бірок, так і з навколишніх сіл яких перевозить шкільний автобус.</p>
