				<div class="hblock">
				<h3>Історія</h3>
				<p>Перша згадка про школу в архівних документах зустрічається від 22 жовтня 1849 р., згодом 12 листопада 1873 р. вона реорганізована в трикласну школу[1]. Про вчительство кінця ХІХ — початку ХХ ст. відомостей мало. Відомо, що в 1885 р. у школі працювали Володимр Балей та його дружина Ірена Швейковська — батьки академіка Степана Балея. При школі в цей час працювало 2-4 вчителі, один з яких був директором або, як тоді називали, управителем. Перша будівля школи знаходилась у Великих Бірках у будинку № 188 (сучасна адреса — вул. Шевченка, 76), і до наших днів не збереглась. Наприкінці ХІХ — на початку ХХ ст, приміщення вже 4-класної початкової школи перенесли в центральну частину села,тепер будинок по вул. Грушевського, 57 і знаходилася там до 1936 року, коли було відкрито 7-класну школу. Після відновлення Польської держави в селі не було своєї національної школи, лише початкова народна з польською мовою навчання. В кінці 20-х років українська громада добилася, щоб школа була змішаною польсько-українською. В середині 30-х років на кошти сільської громади була побудована нова школа з польсько-українською мовою навчання. На період з 02.02.1936 р. була ще 4-ох класна, а на 1938 р. 7-ми класна польсько-українська школа. З 17 вересня 1939 р. зі вступом радянських військ на територію Західної України почалася радянізація української школи. У Великобірківській семирічній школі на той час навчалося 250 учнів і викладало 11 вчителів. Учні школи боролися на фронтах Другої світової війни, були учасниками підпілля ОУН та визвольних змагань УПА. Ще до закінчення війни у вересні 1944 року 298 учнів сіли за парти, їх навчало 14 вчителів. У післявоєнні десятиліття в школі навчалося понад 400 учнів, у наступні роки їх кількість продовжувала зростати. У 1986 р. було 5 перших класів і у них навчався 141 учень. Це найбільше у порівнянні з іншими роками. За період з 1950 по 2000 роки Великобірківська середня школа надала середню освіту 2693 випускникам, з них 69 золотих і 64 срібних медалісти. У Великобірківській середній школі (з 1993 р. ЗОШ І-ІІІ ст., з листопада 1997 р. ім. С Балея) в старших класах навчались і здобували середню освіту також значна кількість учнів старших класів з навколишніх сіл: Дичкова, Малого Ходачкова, Смиковець, Романівки, Максимівки, Ступок, Чернелева-Руського . Найбільша кількість учнів що одночасно навчались у школі була у 1995-1996 навчальному році і становила – 734 учні, їх навчали 57 вчителів. У 70-80 - ті роки в школі створена потужна матеріальна навчальна база, працюють майстерні , спеціалізовані класні кабінети, спортивні секції, бібліотека. З 1999 року запроваджено вивчення автосправи.</p>
